#  + , - , * , / , //(integer or floor division), ** (power), %(modulus => reminder)

#example1
a = 10 
b = 3

sum = a+b  #13
diff = a-b  # 7
mult = a*b  # 30
div = a/b # 3.33333335

print(sum)
print(diff)
print(mult)
print(div)


#example2
a = 10 
b = 4

div1 = a/b # 2.5
div2 = a//b #2

print(div1, div2)


# 10^3 (exponential operator)

a = 10 
b = 3 
ans = a**b # 1000

print(ans)



# reminder using modulus

a = -10 
b = -3 
ans = a%b

print(ans)